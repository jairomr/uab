package org.jairo.matos;

public class Ex3Cal {
	public static int soma(int a,int b){
		return a+b;
	}
	public static double div(int a,int b){
		return a/b;
	}
	public static int mult(int a,int b){
		return a*b;
	}
	public static int sub(int a,int b){
		return a-b;
	}
	public static int pot(int a, int b){
		return a^b;
	}

}
